// Helper methods :
//1. document.write('Hello World')
//2. console.log('Hello World')
//3. alert('Hello World')

document.write('Hello World') // - writes in the browser page itself
alert('Hello World') // - popup box
console.log('Hello world') // - writes in the console

document.write({ name: 'john' }) // will print {object Object}
alert({ name: 'john' }) // - will print {object Object}
console.log({ name: 'john' }) // - will print {name : john} - hence this is more powerful in debugging

