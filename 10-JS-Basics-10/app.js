// Switch Statements

// dice values: 1 - 6

const dice = 05;

switch(dice){
    case 1: 
        console.log('Dice value is 1');
        break
    case 2:
         console.log('Dice value is 2');
         break
    case 3: 
        console.log('Dice value is 3');
        break
    case 4: 
        console.log('Dice value is 4');
        break
    case 5: 
        console.log('Dice value is 5');
        break
    case 6: 
        console.log('Dice value is 6');
        break
    default: 
        console.log('You did not roll the dice');
}